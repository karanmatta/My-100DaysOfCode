//Access Modifiers

// The access modifiers are used to control the access to the members of a class.
// There are four access modifiers in Java:
// 1. private
// 2. default
// 3. protected
// 4. public
// The access modifiers are used to set the access level for classes, attributes, methods, and constructors.
// The access level can be:
// 1. private
// 2. default
// 3. protected
// 4. public
// The default access modifier is used when we don't specify any access level.
// The private access modifier is used to declare a variable, method, or constructor accessible only within the same class.
// The public access modifier is used to declare a variable, method, or constructor accessible from any other class.
// The protected access modifier is used to declare a variable, method, or constructor accessible in the same package and subclasses.
// The access modifiers are used to set the access level for classes, attributes, methods, and constructors.
